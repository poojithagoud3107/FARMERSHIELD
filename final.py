# =========================
# VOICE FUNCTION (TELUGU FULL INFO)
# =========================
def speak(info):
    text = f"""
    లక్షణాలు: {info['symptoms']}.
    మందు: {info['medicine']}.
    అత్యవసర సూచనలు: {info['emergency']}.
    """
    tts = gTTS(text=text, lang='te')
    tts.save("voice.mp3")
    st.audio("voice.mp3")

